  webColStat.R_i      = '#FFFFC6'; webColStat.N_i   = '#FFFFC6'; 
 
  webColStat.del_Wb   = '#FFFF9C';  webColStat.del_Wp   = '#FFFF9C';  webColStat.del_V = '#FFFF9C'; webColStat.r_B = '#FFFF9C'; 

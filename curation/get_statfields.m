%% get_statfields
% returns cell array of strings with names of statistics and a stucture
% with color fields which are needed for the web for a given DEB model

%%
function [webStatFields, webColStat]= get_statfields(model)
% created 2016/09/21 by Starrlight Augustine
 
  %% Syntax 
  % [webStatFields, webColStat] = <../get_statfields.m *get_statfields*> (model)

  %% Description
  % Provides the list of fields in statistics in the order that they should
  % be printed to the wb
  %
  % Input
  %
  % * model: string with name of model: 'std', 'stf', 'stx', 'ssj', 'sbp', 'abj', 'asj', 'abp', 'hep', 'hex'. 
  %
  % Output
  %
  % * webStatFields: cell string with field names
  % * webColStat: structure with associated colors for each field name
  
  %% Remarks
  % Only applies to generating webpages - change here if we could like to
  % see other things on the web and/ or wish to change the order they
  % appear in on the web

  %% Example of use
  % [webStatFields, webColStat] = get_parfields(metaPar.model)
  
  webColStat.z        = '#FFC6A5';
  webColStat.s_Hbp    = '#F7BDDE'; webColStat.s_HLbp = '#F7BDDE'; webColStat.s_s   = '#F7BDDE';

    'z'; 'c_T'; ...
    's_Hbp'; 's_HLbp'; 's_s'; ...
    'E_0';'Wd_0'; ...
    'a_b'; 'a_p'; 'a_99'; ...
    'Wd_b'; 'Wd_p'; 'Wd_i'; 'L_b';'L_p'; 'L_i'; ...
    'W_dWm'; 'dWm'; ...
    'R_i'; 'N_i'; ...
    'del_Wb'; 'del_Wp'; 'del_V'; 'r_B'; ...
    'E_m'; 't_starve'; 't_E'; 'xi_WE'; ...
    'eb_min_G'; 'eb_min_R'; ... 'ep_min'; ...
    'J_Ob'; 'J_Op'; 'J_Oi'};

  case 'abj'
    webStatFields =  {...
    'z'; 'c_T'; ...
    's_M'; 's_Hbp'; 's_HLbp'; 's_s'; ...
   'E_0'; 'Wd_0'; ...
    'a_b'; 'a_p'; 'a_99'; ...
    'Wd_b'; 'Wd_p'; 'Wd_i'; 'L_b';'L_p'; 'L_i'; ...
    'W_dWm'; 'dWm'; ...
    'N_i'; 'R_i';  ...
    'del_Wb'; 'del_Wp'; 'del_V'; 'r_B'; ...
    'E_m'; 't_starve'; 't_E'; 'xi_WE'; ...
    'eb_min_G'; 'eb_min_R'; ... 'ep_min'; ...
    'J_Ob'; 'J_Op'; 'J_Oi'};

 
    
  case 'asj'
     webStatFields =  {...
    'z'; 'c_T'; ...
    's_M'; 's_Hbp'; 's_HLbp'; 's_s'; ...
    'E_0';'Wd_0'; ...
    'a_b'; 'a_p'; 'a_99'; ...
    'Wd_b'; 'Wd_p'; 'Wd_i'; 'L_b';'L_p'; 'L_i'; ...
    'W_dWm'; 'dWm'; ...
    'N_i'; 'R_i';  ...
    'del_Wb'; 'del_Wp'; 'del_V'; 'r_B'; ...
    'E_m'; 't_starve'; 't_E'; 'xi_WE'; ...
    'eb_min_G'; 'eb_min_R'; ... 'ep_min'; ...
    'J_Ob'; 'J_Op'; 'J_Oi'};

  case 'hep'
    webColStat.L_e = '#DEF3BD';   
    webStatFields =  {...
    'z'; 'c_T'; ...
    's_M'; 's_Hbp'; 's_HLbp'; 's_s'; ...
    'E_0';'Wd_0'; ...
    'a_b'; 'a_p';  ...
    'Wd_b'; 'Wd_p'; 'Wd_i'; 'L_b';'L_p'; 'L_e'; ...
    'W_dWm'; 'dWm'; ...
    'N_i';  ...
    'del_Wb'; 'del_Wp'; 'del_V';  ...
    'E_m'; 't_starve'; 't_E'; 'xi_WE'; ...
    'eb_min_G'; 'eb_min_R'; ... 'ep_min'; ...
    'J_Ob'; 'J_Op'; 'J_Oi'};

  case 'hex'
     webColStat.L_e = '#DEF3BD';   
     webStatFields =  {...
    'z'; 'c_T'; ...
    's_M';  's_s'; ...
    'E_0';'Wd_0'; ...
    'a_b'; 'a_p';  ...
    'Wd_b'; 'Wd_i'; 'L_b'; 'L_e'; ...
    'W_dWm'; 'dWm'; ...
    'N_i';   ...
    'del_Wb'; 'del_V';  ...
    'E_m'; 't_starve'; 't_E'; 'xi_WE'; ...
    'eb_min_G'; 'eb_min_R'; ... 'ep_min'; ...

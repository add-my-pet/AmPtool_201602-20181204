  webColStat.J_Ob     = '#FFFFFF';  webColStat.J_Op     = '#FFFFFF'; webColStat.J_Oi = '#FFFFFF';

  webColStat.s_M  = '#F7BDDE'; 
  webColStat.a_j  = webColStat.a_b;  webColStat.L_j = webColStat.L_b;  webColStat.Wd_j = webColStat.Wd_b; 
  webColStat.J_Oj = webColStat.J_Ob;  %webColStat.p_t_j = webColStat.p_t_b; webColStat.RQ_j = webColStat.RQ_b;

  
  
  switch model
      
  case {'std', 'abp', 'sbp'}      
    webStatFields =  {...
    'z'; 'c_T'; ...
    's_Hbp'; 's_HLbp'; 's_s'; ...
    'E_0';'Wd_0'; ...
    'a_b'; 'a_p'; 'a_99'; ...
    'Wd_b'; 'Wd_p'; 'Wd_i'; 'L_b';'L_p'; 'L_i'; ...
    'W_dWm'; 'dWm'; ...
    'R_i'; 'N_i'; ...
    'del_Wb'; 'del_Wp'; 'del_V'; 'r_B'; ...
    'E_m'; 't_starve'; 't_E'; 'xi_WE'; ...
    'eb_min_G'; 'eb_min_R'; ... 'ep_min'; ...
    'J_Ob'; 'J_Op'; 'J_Oi'};
    
  case {'stx', 'stf'}     
    webStatFields =  {...
    'z';  'c_T'; ...
    's_Hbp'; 's_HLbp'; 's_s'; ...
    'a_b'; 't_g'; 'a_p'; 'a_99'; ...
    'Wd_b'; 'Wd_p'; 'Wd_i'; 'L_b';'L_p'; 'L_i'; ...
    'W_dWm'; 'dWm'; ...
    'R_i'; 'N_i'; ...
    'del_Wb'; 'del_Wp'; 'del_V'; 'r_B'; ...
    'E_m'; 't_starve'; 't_E'; 'xi_WE'; ...
